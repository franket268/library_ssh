package com.model;

public class Book_copy {
	private int id;

	private int isAvailable;
	private Book book;
	
	
	
	
	public Book_copy(){
    }





	public int getId() {
		return id;
	}





	public void setId(int id) {
		this.id = id;
	}










	public int getIsAvailable() {
		return isAvailable;
	}





	public void setIsAvailable(int isAvailable) {
		this.isAvailable = isAvailable;
	}





	public Book getBook() {
		return book;
	}





	public void setBook(Book book) {
		this.book = book;
	}
	
	
	
}
